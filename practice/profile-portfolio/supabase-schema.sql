create or replace function public.submit_typing_score(
  p_username text,
  p_password text,
  p_cpm integer,
  p_accuracy numeric
)
returns bigint
language plpgsql
security definer
set search_path = pg_catalog, public, extensions
as $$
declare
  v_username text := trim(p_username);
  v_player_id bigint;
  v_score integer;
  v_id bigint;
begin
  if p_cpm is null or p_cpm < 0 or p_cpm > 1200
     or p_accuracy is null or p_accuracy < 0 or p_accuracy > 100 then
    raise exception '유효하지 않은 게임 기록입니다.';
  end if;

  v_player_id := public.verify_player(v_username, p_password);

  if exists (
    select 1 from public.typing_scores
    where player_id = v_player_id
      and created_at > now() - interval '5 seconds'
  ) then
    raise exception '잠시 후 다시 도전해 주세요.';
  end if;

  -- 랭킹 기준은 정확도로 보정하지 않은 실제 분당 타수(CPM)입니다.
  v_score := p_cpm;

  insert into public.typing_scores (player_id, username, cpm, accuracy, score)
  values (v_player_id, v_username, p_cpm, round(p_accuracy, 2), v_score)
  returning id into v_id;

  return v_id;
end;
$$;

