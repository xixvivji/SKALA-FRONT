create extension if not exists pgcrypto;

create table if not exists public.players (
  id bigint generated by default as identity primary key,
  username text not null unique
    check (username ~ '^[A-Za-z0-9가-힣_-]{2,16}$'),
  password_hash text not null,
  created_at timestamptz not null default now(),
  last_active_at timestamptz not null default now()
);

create table if not exists public.guestbook (
  id bigint generated by default as identity primary key,
  player_id bigint not null references public.players(id) on delete cascade,
  username text not null,
  message text not null check (char_length(message) between 1 and 120),
  created_at timestamptz not null default now()
);

create table if not exists public.guestbook_comments (
  id bigint generated by default as identity primary key,
  guestbook_id bigint not null references public.guestbook(id) on delete cascade,
  player_id bigint not null references public.players(id) on delete cascade,
  username text not null,
  message text not null check (char_length(message) between 1 and 120),
  created_at timestamptz not null default now()
);

create table if not exists public.typing_scores (
  id bigint generated by default as identity primary key,
  player_id bigint not null references public.players(id) on delete cascade,
  username text not null,
  cpm integer not null check (cpm between 0 and 1200),
  accuracy numeric(5,2) not null check (accuracy between 0 and 100),
  score integer not null check (score between 0 and 1200),
  created_at timestamptz not null default now()
);

create table if not exists public.blog_admin (
  id text primary key check (id = 'owner'),
  password_hash text not null,
  updated_at timestamptz not null default now()
);

create table if not exists public.daily_logs (
  log_date date primary key,
  title text not null check (char_length(title) between 1 and 36),
  tag text not null check (tag in ('study', 'project', 'daily', 'thought', 'rest')),
  mood text not null check (mood in ('calm', 'good', 'busy', 'tired', 'proud')),
  note text not null default '' check (char_length(note) <= 280),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists guestbook_created_at_idx
  on public.guestbook (created_at desc);
create index if not exists guestbook_comments_guestbook_created_idx
  on public.guestbook_comments (guestbook_id, created_at);
create index if not exists typing_scores_ranking_idx
  on public.typing_scores (score desc, accuracy desc, created_at asc);
create index if not exists typing_scores_player_created_idx
  on public.typing_scores (player_id, created_at desc);
create index if not exists daily_logs_date_idx
  on public.daily_logs (log_date desc);

